var prefix = function (number) {
	var padded = number;
	return padded;
};


var renamePhotos = function () {
	fileOrder.reverse();
	for (var a = 0, b = fileOrder.length; a < b; a += 1) {
		console.log("Renaming ", source + fileOrder[a], " to ", destination + prefix(a) + "_" + fileOrder[a]);
		fs.rename(source + fileOrder[a], destination + prefix(a) + "_" + fileOrder[a] , function(err) {
		    if ( err ) console.log('ERROR: ' + err);
		});
	}
};

// start processing the queue
